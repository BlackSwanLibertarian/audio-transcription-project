import matplotlib.pyplot as plt
import networkx as nx
dining = nx.read_pajek('dining.net')
nx.draw_networkx(dining)

import operator
dc = nx.degree_centrality(dining)
dc_sorted = sorted(dc.items(), key=operator.itemgetter(1),reverse=True)
for key,value in dc_sorted[0:5]:
        print("highest: ", key, value)

indegree_centrality = nx.in_degree_centrality(dining)
outdegree_centrality = nx.out_degree_centrality(dining)


def print_top_5(centrality_dict, centrality_type):
    sorted_dict = sorted(centrality_dict.items(), key=operator.itemgetter(1), reverse=True)
    print(f"Top 5 nodes for {centrality_type} centrality:")
    for key, value in sorted_dict[0:5]:
        print("Node:", key, "Centrality:", value)
    print("\n")

# Using the function
print_top_5(indegree_centrality, "indegree")
print_top_5(outdegree_centrality, "outdegree")
